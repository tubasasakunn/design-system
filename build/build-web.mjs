// Web(Vite)向け生成: sources/ -> dist/web/
//   themes.css  : テーマ別の CSS 変数 (--color-bg-primary など)
//   themes.ts   : テーマトークンの型付きオブジェクト
//   icons/      : 最適化前の SVG コピー (style/category 別)
//   icons.ts    : スタイル別アイコン名の型付き一覧 (IconStyle / IconName)
import { mkdirSync, writeFileSync, copyFileSync, rmSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { loadThemes, loadIcons, themesToCss, findDefaultTheme, defaultIconStyle } from "./lib.mjs";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const ICONS_SRC = join(ROOT, "sources/icons");
const THEMES_SRC = join(ROOT, "sources/themes");
const OUT = join(ROOT, "dist/web");
rmSync(OUT, { recursive: true, force: true });
mkdirSync(join(OUT, "icons"), { recursive: true });

// --- Themes -> CSS 変数 + TS トークン ---
const themes = loadThemes(THEMES_SRC);
const defaultTheme = findDefaultTheme(THEMES_SRC);
const css = "/* AUTO-GENERATED by build/build-web.mjs — 直接編集しないでください */\n" + themesToCss(themes, defaultTheme);
writeFileSync(join(OUT, "themes.css"), css);
writeFileSync(
  join(OUT, "themes.ts"),
  "// AUTO-GENERATED — 直接編集しないでください\n" +
    `export const themes = ${JSON.stringify(themes, null, 2)} as const;\n` +
    "export type ThemeName = keyof typeof themes;\n"
);

// --- Icons -> コピー (style/category 別) + 型付き一覧 ---
const icons = loadIcons(ICONS_SRC);
for (const icon of icons) {
  const dest = join(OUT, "icons", icon.style, icon.category, `${icon.name}.svg`);
  mkdirSync(dirname(dest), { recursive: true });
  copyFileSync(icon.path, dest);
}
// { rounded: ["arrow-left", ...], sharp: [...] } の形でスタイル別に名前を列挙。
const byStyle = {};
for (const icon of icons) (byStyle[icon.style] ??= new Set()).add(icon.name);
const iconsObj = Object.fromEntries(
  Object.keys(byStyle).sort().map((s) => [s, [...byStyle[s]].sort()])
);
const defStyle = defaultIconStyle(ICONS_SRC);
writeFileSync(
  join(OUT, "icons.ts"),
  "// AUTO-GENERATED — 直接編集しないでください\n" +
    `export const icons = ${JSON.stringify(iconsObj, null, 2)} as const;\n` +
    "export type IconStyle = keyof typeof icons;\n" +
    "export type IconName = (typeof icons)[IconStyle][number];\n" +
    `export const defaultIconStyle = ${JSON.stringify(defStyle)} as ${defStyle ? "IconStyle" : "null"};\n`
);

console.log(`✓ web: ${Object.keys(themes).length} themes, ${icons.length} icons → dist/web`);
