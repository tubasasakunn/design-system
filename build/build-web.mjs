// Web(Vite)向け生成: sources/ -> dist/web/
//   themes.css  : テーマ別の CSS 変数 (--color-bg-primary など)
//   themes.ts   : テーマトークンの型付きオブジェクト
//   icons/      : 最適化前の SVG コピー (category 別)
//   icons.ts    : アイコン名の型付き一覧 (IconName)
import { mkdirSync, writeFileSync, copyFileSync, rmSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { loadThemes, loadIcons, themesToCss } from "./lib.mjs";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUT = join(ROOT, "dist/web");
rmSync(OUT, { recursive: true, force: true });
mkdirSync(join(OUT, "icons"), { recursive: true });

// --- Themes -> CSS 変数 + TS トークン ---
const themes = loadThemes(join(ROOT, "sources/themes"));
const css = "/* AUTO-GENERATED by build/build-web.mjs — 直接編集しないでください */\n" + themesToCss(themes);
writeFileSync(join(OUT, "themes.css"), css);
writeFileSync(
  join(OUT, "themes.ts"),
  "// AUTO-GENERATED — 直接編集しないでください\n" +
    `export const themes = ${JSON.stringify(themes, null, 2)} as const;\n` +
    "export type ThemeName = keyof typeof themes;\n"
);

// --- Icons -> コピー + 型付き一覧 ---
const icons = loadIcons(join(ROOT, "sources/icons"));
for (const icon of icons) {
  const dest = join(OUT, "icons", icon.category, `${icon.name}.svg`);
  mkdirSync(dirname(dest), { recursive: true });
  copyFileSync(icon.path, dest);
}
const names = [...new Set(icons.map((i) => i.name))].sort();
writeFileSync(
  join(OUT, "icons.ts"),
  "// AUTO-GENERATED — 直接編集しないでください\n" +
    `export const iconNames = ${JSON.stringify(names, null, 2)} as const;\n` +
    "export type IconName = (typeof iconNames)[number];\n"
);

console.log(`✓ web: ${Object.keys(themes).length} themes, ${icons.length} icons → dist/web`);
